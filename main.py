def sumar_dos_numeros(numero1: int, numero2: int) -> int:
    """
    Suma dos números enteros y retorna el resultado.

    Parámetros:
    numero1 (int): Primer número a sumar.
    numero2 (int): Segundo número a sumar.

    Retorna:
    int: Resultado de la suma.
    """
    return numero1 + numero2


print(sumar_dos_numeros(2, 1))
